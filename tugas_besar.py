def mode_terminal():
    while True:
        print("\n=== APLIKASI QUIZ SEDERHANA ===")
        print("1. Mulai Quiz")
        print("2. Tambah Soal")
        print("3. Riwayat Nilai")
        print("4. Keluar")

        p = input("Pilih: ")

        if p == "1":
            soal = soal_data.copy()
            random.shuffle(soal)
            benar = 0

            for s in soal:
                print("\n" + s["pertanyaan"])
                for i, op in enumerate(s["pilihan"], start=1):
                    print(f"{i}. {op}")

                j = int(input("Jawab: ")) - 1
                if j == s["jawaban_index"]:
                    print("Benar!")
                    benar += 1
                else:
                    print("Salah.")

            skor = round(100 * benar / len(soal), 2)
            print(f"\nHasil: {benar}/{len(soal)} â†’ Nilai {skor}")

            skor_data.append({
                "timestamp": datetime.now().isoformat(timespec='seconds'),
                "skor": skor,
                "benar": benar,
                "total": len(soal)
            })
            save_json(SKOR_FILE, skor_data)

        elif p == "2":
            print("\n=== Tambah Soal ===")
            pert = input("Pertanyaan: ")
            opsi = []
            while True:
                x = input(f"Pilihan {len(opsi)+1} (kosong = selesai): ")
                if x == "":
                    break
                opsi.append(x)

            print("Pilih jawaban benar:")
            for i, o in enumerate(opsi, start=1):
                print(f"{i}. {o}")
            jb = int(input("Jawaban benar: ")) - 1

            soal_data.append({
                "pertanyaan": pert,
                "pilihan": opsi,
                "jawaban_index": jb
            })
            save_json(SOAL_FILE, soal_data)

        elif p == "3":
            print("\n=== Riwayat Nilai ===")
            for e in skor_data:
                print(e)

        elif p == "4":
            print("Keluar...")
            break



